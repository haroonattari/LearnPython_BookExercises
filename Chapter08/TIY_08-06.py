def city_country(city, country):
    return "{0}, {1}".format(city, country)


madinah_saudiarabia = city_country("Madinah", "Kingdom of Saudi Arabia")
ajmer_india = city_country("Ajmer", "India")
karachi_pakistan = city_country("Karachi", "Pakistan")

print(madinah_saudiarabia)
print(ajmer_india)
print(karachi_pakistan)
